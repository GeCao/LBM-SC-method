//filename:文件名, size:字节深度(单位为Byte)
inline GLuint LoadTexture(string filename, int size) // Creates Texture From A Bitmap File
{
	FILE* fp;
	GLubyte* pixels = 0;
	GLuint last_texture_ID = 0, texture_ID = 0;
	GLint width, height, total_bytes;

	fp = fopen(filename.c_str(), "rb");
	if (fp == 0) return 0;
	fseek(fp, 0x0012, SEEK_SET); //定位指针位置到：SEEK_SET(0) + 0x0012(18)处
	fread(&width, 4, 1, fp);//void *width指向一个带有最小尺寸为4(读取的每个元素为4字节)*1(一次读取1个元素)内存块的指针
	fread(&height, 4, 1, fp); height = abs(height);//这里的height读取出来是-512
	fseek(fp, BMP_Header_Length, SEEK_SET);//定位指针位置到：SEEK_SET(0) + BMP_Header_Length(54)处

	GLint line_bytes = width * size; //这是一个RGBA类型的图像，因此需要进行位深度的绑定，也就是4个Byte，32个位。

	total_bytes = line_bytes * abs(height);//总的Byte数：width * height * 位深度

	pixels = (GLubyte*)malloc(total_bytes);//指针的存储单位是Byte
	if (pixels == 0) {
		fclose(fp);
		return 0;
	}
	if (fread(pixels, total_bytes, 1, fp) <= 0) {//fread会返回成功读取的个数。
		free(pixels);
		fclose(fp);
		return 0;
	}

	glGenTextures(1, &texture_ID);//glGenTextures(GLsizei n, GLuint *textures)：生成n个纹理，textures存储纹理索引的第一个元素指针
	if (texture_ID == 0)
	{
		free(pixels);
		fclose(fp);
		return 0;
	}

	glBindTexture(GL_TEXTURE_2D, texture_ID);//绑定纹理

	//纹理过滤函数，减少从纹理图像空间到帧缓存图像空间的失真。
	//四维纹理坐标：(s, t, r, q)
	//缩小过滤，线性过滤(使用距离当前渲染像素中心最近的4个纹理像素加权平均值)。
	glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MIN_FILTER, GL_LINEAR);
	//放大过滤，线性过滤(使用距离当前渲染像素中心最近的4个纹理像素加权平均值)。
	glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MAG_FILTER, GL_LINEAR);
	//二维纹理中的S方向上的贴图方式，GL_REPEAT忽略边框. 从对侧选择一个2X2纹理像素阵列来计算加权平均。
	glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_WRAP_S, GL_REPEAT);
	//二维纹理中的T方向上的贴图方式，GL_REPEAT忽略边框. 从对侧选择一个2X2纹理像素阵列来计算加权平均。
	glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_WRAP_T, GL_REPEAT);
	//纹理映射方式，只有第三个参数需要修改，这里的GL_REPLACE是默认的纹理与材质混合形式
	glTexEnvf(GL_TEXTURE_ENV, GL_TEXTURE_ENV_MODE, GL_REPLACE);//可以选择：GL_REPLACE/GL_BLEND(混合)/GL_DECAL(光照无用)/GL_MODULATE(光照有用)/GL_ADD
	//将Pixels数组中的像素值传给当前绑定的纹理对象，于是便创建了纹理

	if (size == 4)
	{
		glTexImage2D(GL_TEXTURE_2D, 0, GL_RGBA, width, height, 0,//纹理类型，纹理等级，RGBA，宽度，高度，边框大小，
			GL_BGRA_EXT, GL_UNSIGNED_BYTE, pixels);//像素数据格式，像素值的数据类型，像素数据
	}
	else if (size == 3)
	{
		//glTexImage2D(GL_TEXTURE_2D, 0, GL_RGB, width, height, 0,
		//GL_BGR_EXT, GL_UNSIGNED_BYTE, pixels);
		glTexImage2D(GL_TEXTURE_2D, 0, GL_RGB, width, height, 0,
			GL_BGR_EXT, GL_UNSIGNED_BYTE, pixels);
	}

	free(pixels);

	return texture_ID;
}